			<p class="MsoNormal" align="center" style="text-align:center;">
				<b>关于办理非法集资刑事案件适用法律</b><b></b>
			</p>
			<p class="MsoNormal" align="center" style="text-align:center;">
				<b>若干问题的意见</b><b></b>
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				&nbsp;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				各省、自治区、直辖市高级人民法院，人民检察院，公安厅、局，解放军军事法院、军事检察院，新疆维吾尔自治区高级人民法院生产建设兵团分院，新疆生产建设兵团人民检察院、公安局：
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				为解决近年来公安机关、人民检察院、人民法院在办理非法集资刑事案件中遇到的问题，依法惩治非法吸收公众存款、集资诈骗等犯罪，根据刑法、刑事诉讼法的规定，结合司法实践，现就办理非法集资刑事案件适用法律问题提出以下意见：
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<b>一、关于行政认定的问题</b><b></b>
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				行政部门对于非法集资的性质认定，不是非法集资刑事案件进入刑事诉讼程序的必经程序。行政部门未对非法集资作出性质认定的，不影响非法集资刑事案件的侦查、起诉和审判。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				公安机关、人民检察院、人民法院应当依法认定案件事实的性质，对于案情复杂、性质认定疑难的案件，可参考有关部门的认定意见，根据案件事实和法律规定作出性质认定。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				&nbsp;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<b><span>二、关于</span>“向社会公开宣传”的认定问题</b><b></b>
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<span>《最高人民法院关于审理非法集资刑事案件具体应用法律若干问题的解释》第一条第一款第二项中的</span>“向社会公开宣传”，包括以各种途径向社会公众传播吸收资金的信息，以及明知吸收资金的信息向社会公众扩散而予以放任等情形。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				&nbsp;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<b><span>三、关于</span>“社会公众”的认定问题</b><b></b>
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<span>下列情形不属于《最高人民法院关于审理非法集资刑事案件具体应用法律若干问题的解释》第一条第二款规定的</span>“针对特定对象吸收资金”的行为，应当认定为向社会公众吸收资金：
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<span>（一）在向亲友或者单位内部人员吸收资金的过程中，明知亲友或者单位内部人员向不特定对象吸收资金而予以放任的</span>;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				（二）以吸收资金为目的，将社会人员吸收为单位内部人员，并向其吸收资金的。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				&nbsp;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<b>四、关于共同犯罪的处理问题</b><b></b>
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<span>为他人向社会公众非法吸收资金提供帮助，从中收取代理费、好处费、返点费、佣金、提成等费用，构成非法集资共同犯罪的，应当依法追究刑事责任。能够及时退缴上述费用的，可依法从轻处罚</span>;其中情节轻微的，可以免除处罚;情节显著轻微、危害不大的，不作为犯罪处理。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				&nbsp;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<b>五、关于涉案财物的追缴和处置问题</b><b></b>
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				向社会公众非法吸收的资金属于违法所得。以吸收的资金向集资参与人支付的利息、分红等回报，以及向帮助吸收资金人员支付的代理费、好处费、返点费、佣金、提成等费用，应当依法追缴。集资参与人本金尚未归还的，所支付的回报可予折抵本金。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				将非法吸收的资金及其转换财物用于清偿债务或者转让给他人，有下列情形之一的，应当依法追缴：
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<span>（一）他人明知是上述资金及财物而收取的</span>;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<span>（二）他人无偿取得上述资金及财物的</span>;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<span>（三）他人以明显低于市场的价格取得上述资金及财物的</span>;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<span>（四）他人取得上述资金及财物系源于非法债务或者违法犯罪活动的</span>;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				（五）其他依法应当追缴的情形。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				查封、扣押、冻结的易贬值及保管、养护成本较高的涉案财物，可以在诉讼终结前依照有关规定变卖、拍卖。所得价款由查封、扣押、冻结机关予以保管，待诉讼终结后一并处置。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				查封、扣押、冻结的涉案财物，一般应在诉讼终结后，返还集资参与人。涉案财物不足全部返还的，按照集资参与人的集资额比例返还。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				&nbsp;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<b>六、关于证据的收集问题</b><b></b>
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				办理非法集资刑事案件中，确因客观条件的限制无法逐一收集集资参与人的言词证据的，可结合已收集的集资参与人的言词证据和依法收集并查证属实的书面合同、银行账户交易记录、会计凭证及会计账簿、资金收付凭证、审计报告、互联网电子数据等证据，综合认定非法集资对象人数和吸收资金数额等犯罪事实。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				&nbsp;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<b>七、关于涉及民事案件的处理问题</b><b></b>
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				对于公安机关、人民检察院、人民法院正在侦查、起诉、审理的非法集资刑事案件，有关单位或者个人就同一事实向人民法院提起民事诉讼或者申请执行涉案财物的，人民法院应当不予受理，并将有关材料移送公安机关或者检察机关。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				人民法院在审理民事案件或者执行过程中，发现有非法集资犯罪嫌疑的，应当裁定驳回起诉或者中止执行，并及时将有关材料移送公安机关或者检察机关。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				&emsp;&emsp;公安机关、人民检察院、人民法院在侦查、起诉、审理非法集资刑事案件中，发现与人民法院正在审理的民事案件属同一事实，或者被申请执行的财物属于涉案财物的，应当及时通报相关人民法院。人民法院经审查认为确属涉嫌犯罪的，依照前款规定处理。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				&nbsp;
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				<b>八、关于跨区域案件的处理问题</b><b></b>
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				跨区域非法集资刑事案件，在查清犯罪事实的基础上，可以由不同地区的公安机关、人民检察院、人民法院分别处理。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				对于分别处理的跨区域非法集资刑事案件，应当按照统一制定的方案处置涉案财物。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
				国家机关工作人员违反规定处置涉案财物，构成渎职等犯罪的，应当依法追究刑事责任。
			</p>
			<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
