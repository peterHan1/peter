		</p>
		<p class="MsoNormal" style="margin-left:0pt;text-indent:24pt;">
			（一）因违规经营行为被查处或被起诉；
		</p>
		<p class="MsoNormal" style="margin-left:0pt;text-indent:24pt;">
			（二）董事、监事、高级管理人员违反境内外相关法律法规行为；
		</p>
		<p class="MsoNormal" style="margin-left:0pt;text-indent:24pt;">
			（三）国务院银行业监督管理机构、地方金融监管部门等要求的其他情形。
		</p>
		<p class="MsoNormal" style="margin-left:0pt;text-indent:24pt;">
			&nbsp;
