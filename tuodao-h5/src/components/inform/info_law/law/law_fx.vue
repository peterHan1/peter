			<b>三、明确标准、分类施策</b><b></b>
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			（一）分类处置标准
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			<span>专项整治工作的重点是整治和取缔互联网企业在线上线下违规或超范围开展网贷业务，以网贷名义开展非法集资等违法违规活动。分类处置标准以《指导意见》和有关监管要求等作为主要依据：一是网贷机构满足信息中介的定性。二是业务符合直接借贷的标准，即个体与个体之间通过互联网机构实现的直接借贷。三是不得触及业务</span>“红线”，即设立资金池、自融、向出借人提供担保或者承诺保本保息、大规模线下营销、误导性宣传、虚构借款人及标的、发放贷款、期限拆分、发售银行理财和券商资管等产品、违规债权转让、参与高风险证券市场融资或利用类HOMS等系统从事股票市场场外配资行为、从事股权众筹或实物众筹等。四是落实出借人及借款人资金第三方存管要求。五是信息披露完整、客观、及时，并且具备合规的网络安全设施。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			<b>（二）分类处置措施</b><b></b>
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			对各类网贷机构认真甄别，根据风险程度、违法违规性质和情节轻重、社会危害程度大小、处理方式等因素，准确分类，及时纠偏，制定差别化措施，防范处置风险的风险，确保风险全面排查、问题全面整治和监管全面覆盖。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			<span>根据以上标准将网贷机构划分为三类，并实施分类处置。一是合规类。该类机构严格遵守信息中介定位，稳健经营、运作规范，具有较强的管理技术和风险控制能力，基本符合《指导意见》规定，未违反有关法律法规和规章制度。应对此类机构实施持续监管，支持鼓励其合规发展，督促其规范运营。二是整改类。该类机构大多数运行不规范，风险控制不足，缺乏持续经营能力和自我约束能力，大多异化为信用中介，存在触及业务</span>“红线”的问题。此类机构应按照有关要求限期整改，整改不到位的，责令继续整改或淘汰整合，并依法予以处置。三是取缔类。此类机构涉嫌从事非法集资等违法违规活动，应对其严厉打击，坚决实施市场退出，并按照有关法律法规和规章制度规定，由相关部门给予行政处罚或依法追究刑事责任，政府不承担兜底责任。同时，做好核实资本和财务状况工作，妥善处理债权债务关系，依法保护投资者合法权益。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:28.0000pt;text-align:justify;">
			<b>四、职责分工</b><b></b>
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			按照《互联网金融风险专项整治工作实施方案》要求，专项整治工作按照银监会会同中央有关部门与省级人民政府双负责制的原则，明确分工，落实责任。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			（一）加强组织领导。银监会会同中央宣传部、中央维稳办、发展改革委、工业和信息化部、公安部、财政部、住房城乡建设部、人民银行、工商总局、法制办、国家网信办、国家信访局、最高人民法院、最高人民检察院成立网贷风险专项整治工作领导小组，银监会为组长单位，工业和信息化部、公安部、国家网信办、工商总局为副组长单位，其他部门为成员单位，网贷风险专项整治工作小组办公室设在银监会。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			（二）中央监管部门职责。银监会作为网贷风险专项整治工作统筹部门，负责总体工作的组织和协调。一是制定规则，即制定网贷行业监管制度和第三方存管等系列配套制度，拟定网贷风险专项整治工作实施方案，明确专项整治工作目标、原则、内容、措施等。二是培训部署，即对专项整治工作进行周密部署，组织开展培训。三是划清界限，即明确网贷业务负面清单，划清网贷机构不得从事的业务边界。四是督导汇总，即加强跨部门、跨地区间协调，研究重大问题、汇总工作报告等。五是在省级人民政府统一领导下，省金融办（局）与银监会省级派出机构共同牵头负责本地区分领域整治工作，共同承担分领域整治任务。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			各相关部门发挥职能作用，密切协作，互通信息，共享资源，形成合力。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			（三）各省级人民政府职责。各省级人民政府按照中央监管部门的统一方案和要求，负责本地区具体整治工作。在各省级人民政府统一领导下，设网贷风险专项整治联合工作办公室，由省金融办（局）和银监会省级派出机构共同负责，办公室成员由省级人民政府根据工作需要确定相关部门组成，具体组织实施专项整治工作，并建立风险事件应急制度和处置预案，做好本地区维稳工作，最大限度预防和减少风险事件造成的不良社会影响，维护社会稳定。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:28.0000pt;text-align:justify;">
			<b>五、时间进度</b><b></b>
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			<span>（一）部署培训阶段。根据《互联网金融风险专项整治工作实施方案》要求，银监会协调有关各方汇总网贷行业机构基本数据统计，部署培训各地方开展专项整治工作。此项工作于</span>2016年4月底前完成。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			<span>（二）行业摸底排查阶段。各省级人民政府依照网贷行业机构基本数据统计对本地区机构进行摸底排查，并报银监会。此项工作于</span>2016年7月底前完成。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			<span>（三）分类处置阶段。各省级人民政府依照摸底排查结果，结合《指导意见》和本方案要求，对本地区机构进行分类处置。此项工作于</span>2016年11月底前完成。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			<span>（四）总结督导阶段。银监会将适时赴各地对专项整治工作进行督导，各省级人民政府应对检查、查处、整改情况进行总结，形成报告报送银监会。银监会将根据各地情况，形成规范整治工作总体报告，报送互联网金融风险专项整治工作领导小组办公室。此项工作于</span>2017年1月底前完成。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:28.0000pt;text-align:justify;">
			<b>六、配套支持措施</b><b></b>
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			（一）加强舆论宣传引导。加强网贷风险专项整治工作正面宣传与舆论引导，鼓励网贷机构在依法合规的前提下创新发展。通过以案说法，厘清合法和非法的界限，适时主动发声，及时回应投资者关切。加强舆情监测，强化媒体责任，为整治工作营造良好的舆论环境。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			（二）加强各方协调配合。加强各部门沟通协调，完善工作机制，坚持部门间和区域间纵横联动，协作配合。加强中央与地方金融监管协同配合，共同履行好监管职责，形成专项整治和日常监管的合力，确保中央和地方金融监管目标和规则的一致性，守住不发生系统性区域性金融风险的底线。
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			&nbsp;
		</p>
		<p class="MsoNormal" align="justify" style="margin-left:0.0000pt;text-indent:24.0000pt;text-align:justify;">
			（三）注重工作方式方法。专项整治工作具有政策性强、涉及面广、敏感度高、难度较大等特点，要讲究整治策略，注意方式方法，做好风险隔离，依法依规，有节有度，妥善化解各类存量风险，防范风险蔓延和叠加，切实防范处置风险的风险，依法保护投资者合法权益，维护正常的经济金融秩序和社会稳定。
		</p>
